package giris;

public class _02_data_types {
    public static void main(String[] args) {
        // Integer Data Types
        byte vByte = 100;
        System.out.println(vByte);
        short vShort = 10000;
        System.out.println(vShort);
        int vInt = 100000;
        System.out.println(vInt);
        long vLong = 10000000;
        System.out.println(vLong);

        // Float data types
        float vFloat = 3.14159F;
        System.out.println(vFloat);
        double vDouble = 33.333333333;
        System.out.println(vDouble);

        //Char data type
        char c1 = 'J';
        char c2 = 'A';
        char c3 = 'V';
        char c4 = 'A';

        System.out.println("" + c1 + c2 + c3 + c4);

        //Boolean data type
        boolean status = true;
        System.out.println(status);
        status = false;
        System.out.println(status);

        //String data type
        String vString = "Just Do It!";
        System.out.println(vString);
    }
}
